<?php

namespace App\DataTables\Admin;

use App\DataTables\DataTable as DataTable;
use App\Models\Admin\Options;
use \Illuminate\Contracts\View\Factory;

class OptionsDataTable extends DataTable
{
    // para traduzir tem q ter o construtor
    public function __construct(\Yajra\Datatables\Datatables $datatables, Factory $viewFactory) {
        parent::__construct($datatables, $viewFactory);
    }

    /**
     * @return \Illuminate\Http\JsonResponse
     */
    public function ajax()
    {
        return $this->datatables
            ->eloquent($this->query())
            ->addColumn('action', 'admin.options.datatables_actions')
            ->make(true);
    }

    /**
     * Get the query object to be processed by datatables.
     *
     * @return \Illuminate\Database\Query\Builder|\Illuminate\Database\Eloquent\Builder
     */
    public function query()
    {
        $options = Options::query();

        return $this->applyScopes($options);
    }

    /**
     * Optional method if you want to use html builder.
     *
     * @return \Yajra\Datatables\Html\Builder
     */
    public function html()
    {
        return $this->builder()
            ->columns($this->getColumns())
            ->addAction(['width' => '10%'])
            ->ajax('')
            ->parameters([
                'language' => $this->languageOptions,
                'dom' => 'Bfrtip',
                'scrollX' => false,
                'buttons' => [
                    'create',
                    'print',
                    'reset',
                    'reload',
                    [
                         'extend'  => 'collection',
                         'text'    => '<i class="fa fa-download"></i> Export',
                         'buttons' => [
                             'csv',
                             'excel',
                             'pdf',
                         ],
                    ]
                ]
            ]);
    }

    /**
     * Get columns.
     *
     * @return array
     */
    private function getColumns()
    {
        return [
            'key' => ['name' => 'key', 'data' => 'key'],
            'value' => ['name' => 'value', 'data' => 'value']
        ];
    }

    /**
     * Get filename for export.
     *
     * @return string
     */
    protected function filename()
    {
        return 'options';
    }
}
